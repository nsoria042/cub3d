#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int a;

	a = 0;
	if (s == '\0')
		return ;
	while (s[a] != '\0')
	{
		ft_putchar_fd(s[a], fd);
		a++;
	}
}
