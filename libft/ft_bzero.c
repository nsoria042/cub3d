#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	a;
	unsigned char	cc;
	char			*i;

	a = 0;
	cc = '\0';
	i = s;
	while ((a < n))
	{
		*i = cc;
		i++;
		a++;
	}
}
