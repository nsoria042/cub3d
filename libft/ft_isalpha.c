int	ft_isalpha(int c)
{
	unsigned char a;

	a = (unsigned char)c;
	if (c >= 256)
		return (0);
	if (((a >= 'A') && (a <= 'Z')) || ((a >= 'a') && (a <= 'z')))
		return (1);
	return (0);
}
